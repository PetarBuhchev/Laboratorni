##Потребителят въвежда цяло трицифрено пол. число. На база това число се създават два кортежа (tuple).
## В първия елементите са цифрите на числото в прав ред
## Във втория елементите са цифрите на числото в обратен ред
number = int(input())

t1 = tuple(int(x) for x in str(number))
print(t1)
t2 = t1[::-1]
print(t2)