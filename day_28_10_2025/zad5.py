## Въвежда се цяло число, на база него се съставя списък състоящ се от числата от 1
## до N. Въз основа на списъка се създава речник, в този речник, елементите на списъка са ключове
n = int(input())
l = []
for i in range(1,n+1):
    l.append(i)
dic = {}
for i in range(len(l)):
    dic[l[i]] = l[len(l)-i-1]
print(dic)